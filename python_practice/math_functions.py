import math

# Square Root 
number = 25
result = math.sqrt(number)
print("Square root of 25:", result)

# Power 
base = 7
exponent = 2
result = math.pow(base, exponent)
print("7 power 2:", result)

# Absolute Value
number = -123
result = abs(number)
print("Absolute value of -123:", result)

# Pi 
print("Value of Pi:", math.pi)

# Euler's Number 
print("Value of Euler's number (e):", math.e)